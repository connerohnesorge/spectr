// tasksJSONHeader is the comprehensive JSONC comment header prepended to
// tasks.jsonc files. It explains status values, transitions, and workflow.
const tasksJSONHeader = `// Spectr Tasks File (JSONC)
//
// This file contains machine-readable task definitions for a Spectr change.
// JSONC format allows comments while maintaining JSON compatibility.
//
// Status Values:
//   - "pending"     : Task has not been started yet
//   - "in_progress" : Task is currently being worked on
//   - "completed"   : Task has been finished and verified
//
// Status Transitions:
//   pending -> in_progress -> completed
//
//   Tasks should only move forward through these states.
//   Do not skip states or move backward.
//
// Workflow:
//   1. BEFORE starting work on a task, mark it as "in_progress"
//   2. Complete the implementation for the task
//   3. Verify the work is correct and complete
//   4. IMMEDIATELY mark the task as "completed" after verification
//   5. Move to the next task and repeat
//
// IMPORTANT - Update Status Immediately:
//   - Update each task's status IMMEDIATELY after it transitions
//   - Do NOT batch status updates at the end of all work
//   - Do NOT wait until all tasks are done to update statuses
//   - This file should reflect accurate progress at any point in time
//   - Using a single edit to mark a task completed AND the next task
//     in_progress is allowed (this is a single transition, not batching)
//
// Note: This file is auto-generated by 'spectr accept'. Manual edits to
// task status are expected, but structure changes may be overwritten.
// The original tasks.md file is preserved alongside tasks.jsonc to retain
// human-readable formatting, links, and context.
//

`

// buildChildTasksHeader generates the JSONC header for child task files.
func buildChildTasksHeader(changeID, parentTaskID string) string {
	return fmt.Sprintf(`// Generated by: spectr accept %s
// Parent change: %s
// Parent task: %s

`, changeID, changeID, parentTaskID)
}

// writeTasksFile is a shared helper for writing TasksFile structures to disk.
// It marshals the tasksFile to JSON and prepends the provided header.
func writeTasksFile(
	path string,
	tasksFile *parsers.TasksFile,
	header string,
) error {
	jsonData, err := json.MarshalIndent(
		tasksFile,
		"",
		"  ",
	)
	if err != nil {
		return fmt.Errorf(
			"failed to marshal tasks to JSON: %w",
			err,
		)
	}

	// Prepend the header to the JSON data
	output := header + string(jsonData)

	if err := os.WriteFile(path, []byte(output), filePerm); err != nil {
		return fmt.Errorf(
			"failed to write file %s: %w",
			path,
			err,
		)
	}

	return nil
}

// writeTasksJSONC writes tasks to a tasks.jsonc file with header.
// Prepends tasksJSONHeader to the JSON data (see parsers/types.go).
// If appendCfg is provided, appends configured tasks with sequential IDs.
func writeTasksJSONC(
	path string,
	tasks []parsers.Task,
	appendCfg *config.AppendTasksConfig,
) error {
	// Append configured tasks if present
	allTasks := tasks
	if appendCfg != nil && appendCfg.HasTasks() {
		appendedTasks := createAppendedTasks(
			tasks,
			appendCfg,
		)
		allTasks = append(
			allTasks,
			appendedTasks...)
	}

	tasksFile := parsers.TasksFile{
		Version: 1,
		Tasks:   allTasks,
	}

	return writeTasksFile(path, &tasksFile, tasksJSONHeader)
}

// createAppendedTasks creates Task structs from the append config.
// It generates sequential task IDs continuing from the last existing task.
// For example, if the last task ID was 3.2, appended tasks start at 4.1.
func createAppendedTasks(
	existingTasks []parsers.Task,
	cfg *config.AppendTasksConfig,
) []parsers.Task {
	nextSectionNum := findNextSectionNumber(
		existingTasks,
	)
	section := cfg.GetSection()

	tasks := make(
		[]parsers.Task,
		0,
		len(cfg.Tasks),
	)
	for i, desc := range cfg.Tasks {
		taskID := fmt.Sprintf(
			"%d.%d",
			nextSectionNum,
			i+1,
		)
		tasks = append(tasks, parsers.Task{
			ID:          taskID,
			Section:     section,
			Description: desc,
			Status:      parsers.TaskStatusPending,
		})
	}

	return tasks
}

// findNextSectionNumber determines the next section number for appended tasks.
// It parses existing task IDs to find the highest section number, then adds 1.
// Returns 1 if no existing tasks or task IDs are not in expected format.
func findNextSectionNumber(
	tasks []parsers.Task,
) int {
	maxSection := 0
	for _, task := range tasks {
		// Parse section number from task ID (e.g., "3.2" -> 3)
		parts := strings.Split(task.ID, ".")
		if len(parts) < 1 {
			continue
		}
		sectionNum, err := strconv.Atoi(parts[0])
		if err != nil {
			continue
		}
		if sectionNum > maxSection {
			maxSection = sectionNum
		}
	}

	return maxSection + 1
}

// buildTaskStatusMap reads existing tasks.jsonc (v1 or v2) and child files
// to create a map from task ID to status value. This enables status preservation
// when re-running accept after manual status edits.
func buildTaskStatusMap(changeDir string) map[string]parsers.TaskStatusValue {
	statusMap := make(map[string]parsers.TaskStatusValue)

	if err := readRootTaskStatus(changeDir, statusMap); err != nil {
		// No existing file or parse error - return empty map
		return statusMap
	}

	readChildTaskStatus(changeDir, statusMap)

	return statusMap
}

// readRootTaskStatus reads the root tasks.jsonc and extracts statuses.
func readRootTaskStatus(changeDir string, statusMap map[string]parsers.TaskStatusValue) error {
	tasksJSONPath := filepath.Join(changeDir, "tasks.jsonc")
	data, err := os.ReadFile(tasksJSONPath)
	if err != nil {
		return err
	}

	jsonData := stripJSONCComments(data)

	var tasksFile parsers.TasksFile
	if err := json.Unmarshal(jsonData, &tasksFile); err != nil {
		return err
	}

	for _, task := range tasksFile.Tasks {
		statusMap[task.ID] = task.Status
	}

	// Return version info via error to indicate v2 format
	if tasksFile.Version == 2 && len(tasksFile.Includes) > 0 {
		return nil
	}

	return nil
}

// readChildTaskStatus reads child tasks-*.jsonc files (v2 format).
func readChildTaskStatus(changeDir string, statusMap map[string]parsers.TaskStatusValue) {
	pattern := filepath.Join(changeDir, "tasks-*.jsonc")
	matches, err := filepath.Glob(pattern)
	if err != nil {
		return
	}

	for _, childPath := range matches {
		childData, err := os.ReadFile(childPath)
		if err != nil {
			continue
		}

		childJSON := stripJSONCComments(childData)
		var childFile parsers.TasksFile
		if err := json.Unmarshal(childJSON, &childFile); err != nil {
			continue
		}

		for _, task := range childFile.Tasks {
			statusMap[task.ID] = task.Status
		}
	}
}

// stripJSONCComments removes comments from JSONC data to produce valid JSON.
// This is a simple implementation that removes:
// - // single-line comments
// - /* multi-line comments */
//
//nolint:revive // cognitive-complexity,max-control-nesting: JSONC parsing requires state machine
func stripJSONCComments(data []byte) []byte {
	var result []byte
	inString := false
	escape := false
	i := 0

	for i < len(data) {
		c := data[i]

		// Track string boundaries (respect escapes)
		if c == '"' && !escape {
			inString = !inString
			result = append(result, c)
			i++

			continue
		}

		if inString {
			if c == '\\' {
				escape = !escape
			} else {
				escape = false
			}
			result = append(result, c)
			i++

			continue
		}

		// Outside strings - check for comments
		if c == '/' && i+1 < len(data) {
			next := data[i+1]

			// Single-line comment //
			if next == '/' {
				// Skip until newline
				i += 2
				for i < len(data) && data[i] != '\n' {
					i++
				}

				continue
			}

			// Multi-line comment /*
			if next == '*' {
				// Skip until */
				i += 2
				for i+1 < len(data) {
					if data[i] == '*' && data[i+1] == '/' {
						i += 2

						break
					}
					i++
				}

				continue
			}
		}

		result = append(result, c)
		i++
	}

	return result
}

// applyStatusPreservation updates task statuses from the status map.
// Only updates tasks that exist in the map (preserves manual edits).
func applyStatusPreservation(
	tasks []parsers.Task,
	statusMap map[string]parsers.TaskStatusValue,
) {
	for i := range tasks {
		if status, exists := statusMap[tasks[i].ID]; exists {
			tasks[i].Status = status
		}
	}
}

// aggregateSectionStatus computes the overall status for a section based on
// its child tasks. Rules:
// - All completed -> completed
// - Any in_progress -> in_progress
// - Otherwise -> pending
func aggregateSectionStatus(
	tasks []parsers.Task,
	statusMap map[string]parsers.TaskStatusValue,
) parsers.TaskStatusValue {
	if len(tasks) == 0 {
		return parsers.TaskStatusPending
	}

	allCompleted := true
	anyInProgress := false

	for _, task := range tasks {
		// Use status from map if available, otherwise use task's current status
		status := task.Status
		if mapStatus, exists := statusMap[task.ID]; exists {
			status = mapStatus
		}

		if status == parsers.TaskStatusInProgress {
			anyInProgress = true
		}
		if status != parsers.TaskStatusCompleted {
			allCompleted = false
		}
	}

	if allCompleted {
		return parsers.TaskStatusCompleted
	}
	if anyInProgress {
		return parsers.TaskStatusInProgress
	}

	return parsers.TaskStatusPending
}

// writeHierarchicalTasksJSONC writes a hierarchical v2 task structure.
// Creates a root tasks.jsonc with section-level tasks that reference
// child tasks-{N}.jsonc files containing the actual task details.
func writeHierarchicalTasksJSONC(
	changeDir, changeID string,
	sections []sectionGroup,
	statusMap map[string]parsers.TaskStatusValue,
) error {
	// Build root tasks with children references
	rootTasks := make([]parsers.Task, 0, len(sections))

	for _, section := range sections {
		// Skip empty sections
		if len(section.tasks) == 0 {

			continue
		}

		// Skip section 0 (preliminary tasks) - include them directly in root
		if section.sectionNum == "0" {
			// Add preliminary tasks directly to root without children
			applyStatusPreservation(section.tasks, statusMap)
			rootTasks = append(rootTasks, section.tasks...)

			continue
		}

		// Create child file reference
		childFileName := fmt.Sprintf("tasks-%s.jsonc", section.sectionNum)

		// Create root task for this section
		sectionStatus := aggregateSectionStatus(section.tasks, statusMap)
		rootTask := parsers.Task{
			ID:          section.sectionNum,
			Section:     section.sectionName,
			Description: fmt.Sprintf("%s tasks", section.sectionName),
			Status:      sectionStatus,
			Children:    fmt.Sprintf("$ref:%s", childFileName),
		}
		rootTasks = append(rootTasks, rootTask)

		// Write child file
		childPath := filepath.Join(changeDir, childFileName)
		applyStatusPreservation(section.tasks, statusMap)

		childFile := parsers.TasksFile{
			Version: 2,
			Parent:  section.sectionNum,
			Tasks:   section.tasks,
		}

		header := buildChildTasksHeader(changeID, section.sectionNum)
		if err := writeTasksFile(childPath, &childFile, header); err != nil {
			return fmt.Errorf("failed to write child file %s: %w", childFileName, err)
		}
	}

	// Write root tasks.jsonc
	rootFile := parsers.TasksFile{
		Version:  2,
		Tasks:    rootTasks,
		Includes: []string{"tasks-*.jsonc"},
	}

	rootPath := filepath.Join(changeDir, "tasks.jsonc")

	return writeTasksFile(rootPath, &rootFile, tasksJSONHeader)
}
